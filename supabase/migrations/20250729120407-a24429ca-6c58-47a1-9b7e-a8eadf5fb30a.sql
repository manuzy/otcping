-- Create data_chains table
CREATE TABLE public.data_chains (
    id UUID NOT NULL DEFAULT gen_random_uuid() PRIMARY KEY,
    name TEXT NOT NULL,
    chain_id INTEGER NOT NULL UNIQUE,
    chain_id_hex TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now(),
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now()
);

-- Create data_tokens table
CREATE TABLE public.data_tokens (
    id UUID NOT NULL DEFAULT gen_random_uuid() PRIMARY KEY,
    name TEXT NOT NULL,
    symbol TEXT NOT NULL,
    address TEXT NOT NULL,
    chain_id INTEGER NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now(),
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now(),
    FOREIGN KEY (chain_id) REFERENCES public.data_chains(chain_id)
);

-- Add indexes for performance
CREATE INDEX idx_data_chains_chain_id ON public.data_chains(chain_id);
CREATE INDEX idx_data_chains_name ON public.data_chains(name);
CREATE INDEX idx_data_tokens_chain_id ON public.data_tokens(chain_id);
CREATE INDEX idx_data_tokens_symbol ON public.data_tokens(symbol);
CREATE INDEX idx_data_tokens_address ON public.data_tokens(address);
CREATE INDEX idx_data_tokens_name ON public.data_tokens(name);

-- Enable Row Level Security
ALTER TABLE public.data_chains ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.data_tokens ENABLE ROW LEVEL SECURITY;

-- Create public read policies for reference data
CREATE POLICY "Anyone can view chains" ON public.data_chains FOR SELECT USING (true);
CREATE POLICY "Anyone can view tokens" ON public.data_tokens FOR SELECT USING (true);

-- Add update triggers for timestamps
CREATE TRIGGER update_data_chains_updated_at
    BEFORE UPDATE ON public.data_chains
    FOR EACH ROW
    EXECUTE FUNCTION public.update_updated_at_column();

CREATE TRIGGER update_data_tokens_updated_at
    BEFORE UPDATE ON public.data_tokens
    FOR EACH ROW
    EXECUTE FUNCTION public.update_updated_at_column();

